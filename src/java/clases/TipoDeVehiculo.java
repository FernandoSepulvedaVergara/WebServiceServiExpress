
package clases;

public class TipoDeVehiculo {
    
    private int id_tipo_de_vehiculo;
    private String tipo_de_vehiculo;

    public TipoDeVehiculo() {
        this.id_tipo_de_vehiculo = 0;
        this.tipo_de_vehiculo = null;
    }

    public int getId_tipo_de_vehiculo() {
        return id_tipo_de_vehiculo;
    }

    public void setId_tipo_de_vehiculo(int id_tipo_de_vehiculo) {
        this.id_tipo_de_vehiculo = id_tipo_de_vehiculo;
    }

    public String getTipo_de_vehiculo() {
        return tipo_de_vehiculo;
    }

    public void setTipo_de_vehiculo(String tipo_de_vehiculo) {
        this.tipo_de_vehiculo = tipo_de_vehiculo;
    }

    
}
